import React, { useState } from "react";

export default function KycUpload({ user }) {
  const [idFile, setIdFile] = useState(null);
  const [selfieFile, setSelfieFile] = useState(null);
  const [status, setStatus] = useState("");

  const submitKyc = async (e) => {
    e.preventDefault();
    if (!idFile || !selfieFile) return alert("Please upload both images.");

    const formData = new FormData();
    formData.append("userId", user.id);
    formData.append("idImage", idFile);
    formData.append("selfieImage", selfieFile);

    const res = await fetch("/api/kyc/upload", {
      method: "POST",
      body: formData,
    });

    const data = await res.json();
    if (data.success) {
      setStatus(
        data.result === "VERIFIED"
          ? "✅ KYC Verified Successfully!"
          : `❌ KYC Rejected: ${data.reason}`
      );
    } else setStatus("KYC Upload Failed.");
  };

  return (
    <div className="max-w-md mx-auto mt-8 bg-white p-6 rounded shadow">
      <h2 className="text-xl font-semibold mb-4 text-center">KYC Verification</h2>
      <form onSubmit={submitKyc} className="flex flex-col gap-4">
        <label className="block">
          <span className="text-gray-700">Government ID:</span>
          <input
            type="file"
            accept="image/*"
            onChange={(e) => setIdFile(e.target.files[0])}
            className="mt-1 block w-full"
          />
        </label>
        <label className="block">
          <span className="text-gray-700">Selfie Photo:</span>
          <input
            type="file"
            accept="image/*"
            onChange={(e) => setSelfieFile(e.target.files[0])}
            className="mt-1 block w-full"
          />
        </label>
        <button
          type="submit"
          className="bg-green-600 hover:bg-green-700 text-white py-2 rounded"
        >
          Submit KYC
        </button>
      </form>
      {status && <p className="mt-4 text-center">{status}</p>}
    </div>
  );
}A